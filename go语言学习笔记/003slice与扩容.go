package main

import "fmt"

func main() {
	x := make([]int, 0, 5)
	for i := 0; i < 8; i++ {
		x = append(x, i)
		fmt.Println(&x[0], ":", x, cap(x))
	}
	fmt.Println(x)
}

//输出：
//0xc00008c030 : [0] 5
//0xc00008c030 : [0 1] 5
//0xc00008c030 : [0 1 2] 5
//0xc00008c030 : [0 1 2 3] 5
//0xc00008c030 : [0 1 2 3 4] 5
//0xc000068050 : [0 1 2 3 4 5] 10
//0xc000068050 : [0 1 2 3 4 5 6] 10
//0xc000068050 : [0 1 2 3 4 5 6 7] 10
//[0 1 2 3 4 5 6 7]
//前五项输出，切片第0个元素的地址都没变，第六个开始发生了自动扩容，地址改变了
