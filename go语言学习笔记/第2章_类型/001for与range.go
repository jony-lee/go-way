package main

import "fmt"

//range关键字的用法及避坑实践
func main() {
	x := []int{100, 101, 102}
	for i, n := range x {
		fmt.Println("1.", i, ":", n)
	}
	for i, n := range x {
		fmt.Println("2.", &i, ":", &n)
	}
}

//输出：
//1. 0 : 100
//1. 1 : 101
//1. 2 : 102
//2. 0xc0000540d0 : 0xc0000540d8
//2. 0xc0000540d0 : 0xc0000540d8
//2. 0xc0000540d0 : 0xc0000540d8
//从例1可以看出，range返回迭代对象的两个部分，一个是序号，另一个是值
//从例2可以看出，for遍历时的临时变量i和n地址不变，每次遍历都需要将相应的值拷贝到临时变量中，而非直接访问对象地址
